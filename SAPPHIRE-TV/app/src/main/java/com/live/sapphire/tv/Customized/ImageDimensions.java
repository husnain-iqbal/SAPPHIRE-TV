public class ImageDimensions {
    private int mWidth;
    private int mHeight;

    public ImageDimensions(int width, int height) {
        mWidth = width;
        mHeight = height;
    }

    public int getWidth() {
        return mWidth;
    }

    public int getHeight() {
        return mHeight;
    }
}
